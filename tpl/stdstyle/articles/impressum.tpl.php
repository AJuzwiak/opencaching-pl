<h1>Cel projektu</h1>
<p>Pomysł i idea polskiego serwisu, który zaproponował sp2ong (Waldek Ogonowski), powstał w 2006. Bazując na kodzie opensource
OpenCaching sp2ong uruchomił 26 maja 2006 polską baze skrzynek OpenCaching PL.
</p>
<p>Geocaching jest jedną z nowych dyscyplin sportowych, która nie ma jeszcze uregulowanych stałych zasad.
Geocaching przy tym wykorzystuje nowe innowacyjne technologie, które w ostatnich latach rozwinęły się:
np. internet, GPS. 
Opencaching.pl powinien być atrakcyjną "non Profit" alternatywą dla istniejących już usług Geocaching. 
Oprócz tego Opencaching.pl oferuje dla wszystkich korzystających otwartą platformę szczególnie dla tych 
którzy chcą brać udział w dalszym rozwoju tego sportu. Opencaching
oferuje wszystkie podstawowe dane w formie które można łatwo obrabiać przez inne aplikacje
poprzez pobrania tych danych. 
OpenCaching PL stanowi jeden z elementów grupy serwerów używających OpenCaching w ramach której dane będą dostępne z innych OC
na lokalnym OC serwerze. W ramach rozwoju OC, twórcy projektu chcą doprowadzić do synchronizacji serwerów OC
w innych krajach i dzięki temu będą stanowić wspólną sieć OC.
Zaletą OpenCaching jest to że od użytkowników zależy kształt, funkcjonlaność, zawartość lokalnego serwisu. To oni tworzą
zasady i mają wpływ na kształt zawartości bazy skrzynek.</p>

<p>
Oferta internetowa Opencaching została stworzona przez wspólną pracę wielu ludzi bez wynagrodzenia, 
z pewnymi wyjątkami, gdzie przytoczony jest autor. Za te imienne wkłady odpowiada każdorazowo autor.</p>

