		<p><b>Współrzędne:</b> Pozycja skrzynki (pobrana z GPS) w układzie WGS-84, docelowego miejsca, obszaru lub parkingu. Jeśli współrzędne nie wskazują pozycji skrzynki
		wszystko co jest potrzebne do znalezienia skrzynki powinno być w opisie.</p>
		<p><b>Kraj:</b> Na terenie jakiego kraju jest ulokowana skrzynka.</p>
		<p><b>Poziom trudności:</b> Zadań i Terenu - w skali od 1 do 5 ocenia się jak trudne są zadania związane z szukaniem i jak
		głęboko w tym terenie jest ukryta skrzynka. (1 - najłatwiejszy, 5 najtrudniejszy)
		<p><a name="time"></a><b>Czas i droga:</b> Jak dużo czasu zajmie przejście od punktu startowego (np schroniska, parkingu) do miejsca gdzie jest
		schowana skrzynka i z powrotem do punktu startowego (czyli jeśli szacowany czas z parkingu do skrzynki jest 1 godzine to podajemy 
		wartość 2 godziny gdyż 1 godzine potrzebujemy na powrót). Oszacowany czas musi być dla wprawionego poszukiwacza.
		Odległość w km podajemy w jedną strone, czyli np z parkingu do skrzynki 2.5 km taką wartość wpisz. Jeśli jednak nie jesteś wstanie
		ocenić czasu i odległości wpisz wartości zero w obu polach</p>
