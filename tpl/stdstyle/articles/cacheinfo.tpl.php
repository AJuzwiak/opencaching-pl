	<p><b>Krótki opis skrzynki:</b> Moższ opisać w paru słowach skrzynkę np informacje o miejscu, terenie itp max 120 znaków</p>
		<p><b>Opis skrzynki:</b> Pełny opis skrzynki. Powinny tu znaleźć się niezbędne informacje dla poszukiwacza aby udało mu się znaleźć skrzynkę (np potrzebne narzędzia, środek lokomocji itp).</p>
		<p><b>Dodatkowe informacje:</b>W tym miejscu możesz podać dodatkowe pomocnicze informacje dla tych którym podstawowy opis nie wystarczy. Informacja
		zostanie odkodowana po kliknięciu na DEKODUJ.</p>
		<p><b>Data ukrycia:</b> Wpisujemy date ukrycia skrzynki. Jeśli data jest w przyszłości to skrzynka automatycznie dostaje status "tymczasowo niedostępna".
		Musisz zmienić jej status kiedy będzie w tym dniu ukryta. <b>Uwaga ta nie dotyczy skrzynek typu wydarzenie gdzie w miejscu daty ukrycia
		należy wpisać date rozpoczęcia wydarzenia.</b></p>
		<p><a name="logpw"></a><b>Hasło do wpisu do Logu:</b> Każdy kto chce zrobić wpis do LOGU musi podać to hasło które
		tu wprowadzisz - dotyczy to tylko wpisów typu ZNALEZIONA. Gdy to pole zostawisz puste nie będzie wymagane hasło aby dokonać wpisu ZNALEZIONA.
		</p>			
