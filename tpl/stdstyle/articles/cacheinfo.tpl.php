		<p><b>Nazwa:</b> Każda skrzynka otrzymuje nazwę. Kilka skrzynek może mieć identyczną nazwę
		ale powinno się tego unikać. Wybierz nazwę skrzynki i unikaj mało
		znaczących nazw jak np. "A81#589". Jedyne ograniczenie dla nazwy skrzynki to długość
		nazwy, która może wynosić max 60 znaków.</p>
		<p>

<b>Wielkość skrzynki</b><p>
<ul><ul>
    <li> Mikro (pojemnik po filmie 35 mm lub mniejszy, typowo zawiera tylko dziennik zwinięty w rulon)
        <li> Mała (Pojemniki plastikowe na kanapke itp lub mniejsze, zawiera dziennik i drobne skarby)
	    <li> Normalna (Pojemniki większe od tych na kanapki lub podobne)
	    <li> Duża i bardzo duża (3 litrowe wiaderko np po farbach lub większe)
</ul></ul>		<p>
