<p>Co nowego w OC PL:
<ul>
<li>Dodane zostały atrybuty skrzynki które można zaznaczać podaczas tworzenia skrzynki lub wykorzystać je w kryteriach szukania
opisy znaczenia atrybutów patrz <a href=articles.php?page=cacheinfo#atrybut>TUTAJ</a>
<li>W ramach ustawień swojego konta możemy ustawić powiadamianie o nowych skrzynkach pojawiajacych się w obrębie zdefiniowanego promienia max 150 km od podanych współrzędnych w swoim profilu
podanie 0 km oznacza wyłącznie powiadamiania.
<li>Dodano możliwość rekomendowania skrzynek, każdy za każde znalezione 10 skrzynek otrzymuje 1 punkt który może podczas wpisywania do LOGU zarekomendować daną skrzynkę.
Skrzynka która otrzyma rekomendacje w jej opise pojawia się zielona gwiazdka i liczba rekomendacji. Na stronie głównej na pod wykazem najnowszych skrzynek
jest wykaz najnowszych rekomendowanych skrzynek z liczbą rekomendacji.
<li> Wprowadzono możliwość dodawania i wyświetlania emotikonek 
<li> Dołożono EDYTOR ktory podczas edycji opisów skrzynki pozwala dowolnie formatować teskt, kolorwać itd z efektem widocznym odrazu na ekranie bez
znajomości kodu HTML
<li>Podczas tworzenia skrzynki w części gdzie podajemy datę ukrycia skrzynki jest opcja ustawienia daty i godziny o której będzie skrzynka automatycznie opublikowana przez OC PL
<li>W rmach skrzynki WYDARZENIE zostały zmienione opcje wpisów w LOGu. Mamy obecnie Komentarz/ Uczestniczyłem/ Będę uczestniczył. Jeśli zadeklarujemy 
udział w wydarzeniu (Będę uczestniczył) na stronie opisującej skrzynke zaraz pod Nazwą Skrzynki jest link do LISTA POTENCJALNYCH UCZESTNIKÓw gdzie po kliknięciu na ten link
jest wykaz chętnych zapisanych na to wydarzenie w LOGu 
<li>W ramach opcji szukania dodano kryteria atrybutów skrzynek oraz opcje szukaj frazy w LOGu, OPISIE lub obrazkach
<li>Dołożona możliowść szukania wg nazwy waypoint np OP0001 (ramka z boku pod głównym menu)
<li>Skrzynki które posiadają hasło do wpisu do LOGu teraz to hasło jest wymagane tylko do wpisu typu ZNALEZIONA, inne opcje wpisu nie wymagaja hasła.
</ul>
